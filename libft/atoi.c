#include "libft.h"

int			ft_atoi(const char *str)
{
	int i;
	int nb;
	int s;

	if (strcmp(str, "-2147483648") == 0)
		return (-2147483648);
	i = 0;
	s = 1;
	nb = 0;
	while (str[i] && (str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
			|| str[i] == ' ' || str[i] == '\f'
			|| str[i] == '\r' || str[i] == '\0' || str[i] == '\011'
			|| str[i] == '\012' || str[i] == '\013' || str[i] == '\014'
			|| str[i] == '\015'))
		i++;
	if (str[i] == '-')
		s = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (s * nb);
}
